package allianceTool.entity;

public class Team {
	private Integer teamId;
	private String teamName;
	private String teamRefine;
	private String twoSystemDefense;
	private String threeSystemDefense;

public String getTeamName() {
	return teamName;
  	}
public void setTeamName(String teamName) {
    this.teamName = teamName;
	}
public String getTeamRefine() {
	return teamRefine;
  	}
public void setTeamRefine(String teamRefine) {
    this.teamRefine = teamRefine;
	}
public String getTwoSystemDefense() {
	return twoSystemDefense;
  	}
public void setTwoSystemDefense(String twoSystemDefense) {
    this.twoSystemDefense = twoSystemDefense;
	}
public String getThreeSystemDefense() {
	return threeSystemDefense;
  	}
public void setThreeSystemDefense(String threeSystemDefense) {
    this.threeSystemDefense = threeSystemDefense;
	}
public Integer getTeamId() {
	return teamId;
}
public void setTeamId(Integer teamId) {
	this.teamId = teamId;
}

	  
}
